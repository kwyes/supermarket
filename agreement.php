③ "회사"는 "서비스"의 운영과 관련하여 서비스 화면, 홈페이지, 전자우편 등에 광고를 게재할 수 있습니다. 광고가 게재된 전자우편 등을 수신한 "회원"은 수신거절을 "회사"에게 할 수 있습니다. 
제 15 조 (권리의 귀속)① "서비스"에 대한 저작권 및 지적재산권은 "회사"에 귀속됩니다.
