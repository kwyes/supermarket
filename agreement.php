2. 타인의 정보도용 
3. "회사"가 게시한 정보의 변경 
4. "회사"가 정한 정보 이외의 정보(컴퓨터 프로그램 등) 등의 송신 또는 게시 
5. "회사"와 기타 제3자의 저작권 등 지적재산권에 대한 침해 
6. "회사" 및 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위 
